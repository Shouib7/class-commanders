package week_4.day_1.activities;

public class IfStatement {

    public static void main(String[] args) {

        /*
        * Syntax
        *
        *   if ( condition ) { // Opening block of the if statement
        *
    *      } // Closing block of the if statement
    *       else {
    *
    *       }
        *
        * */

        if ( 50 > 50 ) {
            System.out.println("50 is bigger than 20");
        } else {
            System.out.println( "20 is less than 50" );
        }

        System.out.println( "TekSchool!" );

    }

}
